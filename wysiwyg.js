var personContainer = document.getElementsByClassName("person__container");
let bioArea = document.getElementById("bio");
let textInput = document.getElementById("textObj");
//this variable is created on the global scope so that I can access it in all the variables and so that its value can change each time I call it.
var personId = "";

// in this for loop, I am creating the event listener that is waiting for a click to add a border to the clicked div and place the focus on the text box
for (var i = 0; i < personContainer.length; i++) {
  personContainer[i].addEventListener("click", function (event) {
    // I set the person ID to target the entire div. Using event.target.id would look at every element within the div individually and we don't want that for this purpose.
    	personId = event.currentTarget.id;
    	//because my target was a div, I can target it for addng the border.
    	let getWhole = document.getElementById(personId);
    	//here is where I target the div so when I click it, I will get a border
		getWhole.classList.toggle("border");

		//here I tell the event that after it has added the border to place the cursor (or focus) on the text box.
		textInput.focus();
  		});

};

	//this is another event listener that is focusing on the keyup and running a function that changes the text inside of the elements that have the ID specified in the getelementbyid declaration.
	textInput.addEventListener("keyup", () => {
		//because I made personId available globally and it was redefined in the function above, I am able to access that value here in the element ID so that it ONLY replaces the bio text inside of the clicked div SOOOOO COOL!!!
		document.getElementById(`bio--${personId}`).innerText = textInput.value;

		//this code clears the typed text when the enter button is pressed, but does not refresh or submit the page so that the changes made remain.
		if (event.keyCode == 13) {
		textInput.value = "";
	}
	
});  

